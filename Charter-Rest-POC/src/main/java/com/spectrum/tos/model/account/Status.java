package com.spectrum.tos.model.account;

public class Status {

	private String status;
	private String agcStatus;
	private String cfsStatus;
	private String easStatus;
	private String nbStatus;
	private String extensionStatus;

	public String getStatus() {
		return status;
	}

	public void setStatus(String status) {
		this.status = status;
	}

	public String getAgcStatus() {
		return agcStatus;
	}

	public void setAgcStatus(String agcStatus) {
		this.agcStatus = agcStatus;
	}

	public String getCfsStatus() {
		return cfsStatus;
	}

	public void setCfsStatus(String cfsStatus) {
		this.cfsStatus = cfsStatus;
	}

	public String getEasStatus() {
		return easStatus;
	}

	public void setEasStatus(String easStatus) {
		this.easStatus = easStatus;
	}

	public String getNbStatus() {
		return nbStatus;
	}

	public void setNbStatus(String nbStatus) {
		this.nbStatus = nbStatus;
	}

	public String getExtensionStatus() {
		return extensionStatus;
	}

	public void setExtensionStatus(String extensionStatus) {
		this.extensionStatus = extensionStatus;
	}

}
